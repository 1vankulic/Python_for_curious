bin(20) # 0b10100
hex(20) # 0x14
oct(20) # 0o24

int('0100') # when there is no number because no number, default: 10, when there is 0 its automatic, software recognizes base accordingly
int('0100', 2) # int('number', number_system) 
print(int('0100', 16)) # 100(16)=256(10)


